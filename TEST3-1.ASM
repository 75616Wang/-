CODE	SEGMENT
	ASSUME	CS:CODE
START:	

AGAIN:	MOV	AH,01H
		INT 21H
		CMP	AL,' '
		JE	STOP

		CMP	AL,'A'
		JL	AGAIN
		CMP	AL,'Z'
		JA	NEXT
		ADD	AL,20H

		MOV	AH,2
		MOV	DL,AL
		INT	21H
		JMP	AGAIN

NEXT:	CMP	AL,'a'
		JL	AGAIN
		CMP	AL,'z'
		JA	NEXT
		SUB	AL,20H

		MOV	AH,2
		MOV	DL,AL
		INT	21H

		JMP	AGAIN

STOP:	MOV	AH,4CH
		INT	21H
CODE	ENDS
	END START
