DATA	SEGMENT
NUMCOUNT	DB	30H
CHARCOUNT	DB	30H
OTHERCOUNT	DB	30H
DATA	ENDS

CODE	SEGMENT
	ASSUME	CS:CODE,DS:DATA
START:	
	MOV	AX,DATA
	MOV	DS,AX

	AGAIN:	MOV	AH,01H
		INT 21H
		CMP	AL,' '
		JE	STOP

		CMP	AL,'1'
		JL	OTHER
		CMP	AL,'9'
		JA	NEXT1
		INC	NUMCOUNT
		JMP	AGAIN

NEXT1:	CMP	AL,'A'
		JL	OTHER
		CMP	AL,'Z'
		JA	NEXT2
		INC	CHARCOUNT

		JMP	AGAIN

NEXT2:	CMP	AL,'a'
		JL	OTHER
		CMP	AL,'z'
		JA	OTHER
		INC	CHARCOUNT

		JMP	AGAIN
OTHER:
		INC	OTHERCOUNT
		JMP	AGAIN

		

STOP:	MOV	AH,2
		MOV	DL,10
		INT	21H

		MOV	AH,2
		MOV	AL,NUMCOUNT
		MOV	DL,AL
		INT	21H

		MOV	AH,2
		MOV	DL,10
		INT	21H

		MOV	AH,2
		MOV	AL,CHARCOUNT
		MOV	DL,AL
		INT	21H

		MOV	AH,2
		MOV	DL,10
		INT	21H

		MOV	AH,2
		MOV	AL,OTHERCOUNT
		MOV	DL,AL
		INT	21H

		MOV	AH,4CH
		INT	21H
CODE	ENDS
	END START
